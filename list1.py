list1 = [10, 30, 20, 40]

print("Original list:", list1)

list1.append(50)
print("After adding element:", list1)

list1.remove(30)
print("After removing element:", list1)

list1[1] = 25
print("After modifying element:", list1)

list1.sort()
print("After sorting list:", list1)

list1.reverse()
print("After reversing list:", list1)